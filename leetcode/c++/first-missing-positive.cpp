class Solution {
  public:
    int firstMissingPositive(vector<int> &nums) {
        auto it = remove_if(nums.begin(), nums.end(),
                            [](const int &num) { return num <= 0; });
        int n = distance(nums.begin(), it);
        for (int i = 0; i < n; i++) {
            int num = abs(nums[i]);
            if (num <= n) {
                nums[num - 1] = -abs(nums[num - 1]);
            }
        }
        for (int i = 0; i < n; i++) {
            if (nums[i] > 0) {
                return i + 1;
            }
        }
        return n + 1;
    }
};
