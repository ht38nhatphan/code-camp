/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */

class Solution {
  public:
    int getDecimalValue(ListNode *head) {
        int res = 0;
        while (head) {
            res = 2 * res + head->val;
            head = head->next;
        }
        return res;
    }
};
