#include <vector>
using std::vector;

vector<vector<int>> cartesianNeighbor(int x, int y) {
    vector<vector<int>> res;
    for (int i = -1; i <= 1; i++) {
        for (int j = -1; j <= 1; j++) {
            if (i == 0 && j == 0) {
                continue;
            }
            res.push_back({x + i, y + j});
        }
    }
    return res;
}
